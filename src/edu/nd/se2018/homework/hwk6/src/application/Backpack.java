package application;

public class Backpack {
	int chips;
	int redKeys;
	int blueKeys;
	int greenKeys;
	int yellowKeys;
	
	public Backpack() {
		chips = 0;
		redKeys = 0;
		blueKeys = 0;
		greenKeys = 0;
		yellowKeys = 0;
	}
	public void empty() {
		chips = 0;
		redKeys = 0;
		blueKeys = 0;
		greenKeys = 0;
		yellowKeys = 0;
	}
}
